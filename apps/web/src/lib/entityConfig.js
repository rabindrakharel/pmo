export const formatCurrency = (amount, currency = 'CAD') => {
    if (!amount)
        return '-';
    return new Intl.NumberFormat('en-CA', {
        style: 'currency',
        currency
    }).format(amount);
};
export const renderBadge = (value, colorMap) => {
    const colorClass = colorMap[value] || 'bg-dark-100 text-dark-600';
    return React.createElement('span', { className: `inline-flex items-center px-2.5 py-0.5 rounded-full text-xs font-medium ${colorClass}` }, value);
};
