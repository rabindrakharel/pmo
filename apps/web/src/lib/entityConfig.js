/**
 * Render employee names from array
 * Backend API returns employee names in assignee_employee_names field
 */
export const renderEmployeeNames = (names, record) => {
    // Try to use assignee_employee_names from the record first (populated by backend)
    const employeeNames = record?.assignee_employee_names || names;
    if (!employeeNames)
        return React.createElement('span', { className: 'text-dark-600' }, '-');
    // Handle both array and JSON string formats
    let namesArray = [];
    if (typeof employeeNames === 'string') {
        try {
            namesArray = JSON.parse(employeeNames);
        }
        catch {
            namesArray = [employeeNames];
        }
    }
    else if (Array.isArray(employeeNames)) {
        namesArray = employeeNames;
    }
    if (namesArray.length === 0)
        return React.createElement('span', { className: 'text-dark-600' }, '-');
    return React.createElement('div', { className: 'flex flex-wrap gap-1' }, ...namesArray.slice(0, 2).map((name, index) => React.createElement('span', {
        key: index,
        className: 'inline-flex items-center px-2 py-0.5 rounded text-xs font-medium bg-indigo-100 text-indigo-800'
    }, name)), namesArray.length > 2 ? React.createElement('span', { className: 'text-xs text-dark-700' }, `+${namesArray.length - 2} more`) : null);
};
